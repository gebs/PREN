    public Mat ThinSubiteration1(Mat src) {
        int rows = src.rows();
        int cols = src.cols();
        Mat retval = new Mat();
        src.copyTo(retval);

        for (int i = 0; i < rows; i++) {
            for (int j = 0; j < cols; j++) {
                if (src.get(i, j)[0] == 1.0f) {
                    int neighbor0 = (int) src.get(i - 1, j - 1)[0];
                    int neighbor1 = (int) src.get(i - 1, j)[0];
                    int neighbor2 = (int) src.get(i - 1, j + 1)[0];
                    int neighbor3 = (int) src.get(i, j + 1)[0];
                    int neighbor4 = (int) src.get(i + 1, j + 1)[0];
                    int neighbor5 = (int) src.get(i + 1, j)[0];
                    int neighbor6 = (int) src.get(i + 1, j - 1)[0];
                    int neighbor7 = (int) src.get(i, j - 1)[0];

                    int C = (~neighbor1 & (neighbor2 | neighbor3))
                            + (~neighbor3 & (neighbor4 | neighbor5))
                            + (~neighbor5 & (neighbor6 | neighbor7))
                            + (~neighbor7 & (neighbor0 | neighbor1));

                    if (C == 1){
                        int N1 = (neighbor0 | neighbor1)
                                + (neighbor2 | neighbor3)
                                + (neighbor4 | neighbor5)
                                + (neighbor6 | neighbor7);

                        int N2 = (neighbor1 | neighbor2)
                                + (neighbor3 | neighbor4)
                                + (neighbor5 | neighbor6)
                                + (neighbor7 | neighbor0);

                        int N = Math.min(N1,N2);
                        if (N==2 || N == 3){
                            int c3 = ( neighbor1 | neighbor2 | ~neighbor4) & neighbor3;

                            if (c3 == 0){
                                retval.put(i,j,0.0f);
                            }
                        }
                    }
                }
            }
        }
        return retval;
    }


